void InPWM(){

    RPOR1bits.RP2R = 0; //  output pins
    RPOR1bits.RP3R = 0;

    PR3 = 1474;
    T3CON = 0x8000;

    OC1CON = 0x000E;
    OC1R= 0;
    OC1RS = 0;

    OC2CON = 0x000E;
    OC2R = 0;
    OC2RS = 0;
    return 0;
}